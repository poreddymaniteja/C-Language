// Note down all escape sequences and experiment with them - using Hello World //

#include<stdio.h>
int main()

{

//New line \n //
printf("Hello\nWorld\n");

//Horizontal tab \t //
printf("Hello\tWorld\n");

//Vertical tab \v //
printf("Hello\vWorld\n");

//Carriage return \r //
printf("Hello\rWorld\n");

//back space \b //
printf("Hello\bWorld\n");

//audible alert \a //
printf("Hello\aWorld\n");

//Back slash \\ //
printf("Hello\\World\n");

return 0;
}
